"""
They are used to compare values..

Operator    Name
    ==      Equal.
    !=      Not equal
    >       Greater that
    <       Less Than
    >=      Greater than or equal to
    <=      Lesser than or equal to      
"""

b = 4
c = 3
print(b==c)  # False
print(b!=c)  # True
print(b > c)  # True
print(b < c)  # False
print(b >= c)  # True
print(b <= c)  # False